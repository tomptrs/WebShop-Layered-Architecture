        [HttpPost]
        [ValidateAntiForgeryToken]
        public IActionResult Create([Bind("Id,Name,Price")] Product product)
        {
            if (ModelState.IsValid)
            {
                repo.Insert(product);
                return RedirectToAction(nameof(Index));
            }
            return View(product);
        }

